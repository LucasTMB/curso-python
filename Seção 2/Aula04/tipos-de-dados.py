"""
Tipos de dados:

str - string
int - inteiro
float - real/ponto flutuante
bool - booleano/lógico 
"""
# conversão

print(type(int('10')))
print(type(str(10)))
print(type(float('10')))

# Exercício

# String: nome
print('Lucas Teixeira', type('Lucas Teixeira'))

# Idade: int
print(19, type(19))

# Altura: float
print(1.70, type(1.70))

# É maior de idade: bool
print(19 >= 18, type(19 >= 18))