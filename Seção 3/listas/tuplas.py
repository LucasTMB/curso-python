# tupla é mais eficiente e rápida do que a lista

# não precisa colocar colchetes 
nomes = 'Maria', 'Lucas', 'Luiz' # posso colocar em parenteses tb
# nomes[1] = 'Geraldo' -- não posso mudar, tupla é imutavel
print(nomes[0])

# converter para tuplas
carros = ['Ferrari', 'Audi', 'BMW']
carros = tuple(nomes)